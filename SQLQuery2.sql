ALTER TABLE MENSAGEM
ADD DATAHORAENVIO DATETIME NOT NULL
GO
CREATE PROCEDURE SP_INSERIR_MENSAGEM
@EMAIL NVARCHAR(100),
@ASSUNTO NVARCHAR(100),
@TEXTO NVARCHAR(MAX)
AS
BEGIN
INSERT INTO MENSAGEM(
IDMENSAGEM,
EMAIL,
ASSUNTO,
TEXTO,
DATAHORAENVIO)
VALUES(
NEWID(),
@EMAIL,
@ASSUNTO,
@TEXTO,
GETDATE()
)
END
GO